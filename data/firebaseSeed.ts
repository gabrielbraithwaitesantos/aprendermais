const getYouTubeThumb = (videoUrl?: string | null) => {
  if (!videoUrl) return null;
  const match = /[?&]v=([^&]+)/.exec(videoUrl);
  if (!match?.[1]) return null;
  return `https://i.ytimg.com/vi/${match[1]}/hqdefault.jpg`;
};

